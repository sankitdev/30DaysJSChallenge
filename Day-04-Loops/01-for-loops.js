//Task 1 - Write a program to print numbers from 1 to 10 using a for loop

for (let index = 1; index <= 10; index++) {
  console.log(index);
}

//Task 2 - Write a program to print multiplication of 5 using for loop
for (let i = 1; i <= 10; i++) {
  console.log("5x" + i + "=" + 5 * i);
}
